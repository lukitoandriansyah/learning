import operator

a, b = 12, 7

# Using add() function
print('This is a result for addition of these two variables: ', operator.add(a, b))

# Using sub() function
print('This is a result for subtraction of these two variables: ', operator.sub(a, b))

# Using mul() function
print('This is a result for multiplication of these two variables: ', operator.mul(a, b))
