import operator

a, b = 12, 6

# Using truediv()
print('Result using truediv: ', operator.truediv(a, b))

# Using floordiv()
print('Result using floordiv: ', operator.floordiv(a, b))

# Using pow()
print('Result using pow: ', operator.pow(a, b))

# Using mod()
print('Result using mod: ', operator.mod(a, b))
